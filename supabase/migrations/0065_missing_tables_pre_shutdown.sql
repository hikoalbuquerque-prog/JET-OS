-- ── slot_alertas ─────────────────────────────────────────────────────────────
create table if not exists public.slot_alertas (
  id        bigint generated by default as identity primary key,
  slot_id   text,
  tipo      text,
  msg       text,
  criado_em timestamptz not null default now()
);
create index if not exists idx_slot_alertas_slot on public.slot_alertas (slot_id);
alter table public.slot_alertas enable row level security;
create policy slot_alertas_svc  on public.slot_alertas for all to service_role using (true);
create policy slot_alertas_sel  on public.slot_alertas for select to authenticated using (true);
create policy slot_alertas_ins  on public.slot_alertas for insert to authenticated with check (true);

