-- 0065_missing_tables_pre_shutdown.sql
-- Firestore → Supabase: cria tabelas faltantes + adiciona colunas ausentes
-- em tabelas existentes. Idempotente (IF NOT EXISTS / ADD COLUMN IF NOT EXISTS).
-- ============================================================================

-- ════════════════════════════════════════════════════════════════════════════
-- 1. COLUNAS FALTANTES EM TABELAS EXISTENTES
-- ════════════════════════════════════════════════════════════════════════════

-- gojet_snapshots: schema atual é (id text, data jsonb, criado_em).
-- O scraper Firestore salva cidade, cityId, parkings, bikes, total, savedAt, etc.
alter table public.gojet_snapshots add column if not exists cidade        text;
alter table public.gojet_snapshots add column if not exists city_id       text;
alter table public.gojet_snapshots add column if not exists parkings      jsonb;
alter table public.gojet_snapshots add column if not exists bikes         jsonb;
alter table public.gojet_snapshots add column if not exists total_parkings int;
alter table public.gojet_snapshots add column if not exists total_bikes    int;
alter table public.gojet_snapshots add column if not exists chunked       boolean default false;
alter table public.gojet_snapshots add column if not exists total_chunks  int;
alter table public.gojet_snapshots add column if not exists chunk         int;
alter table public.gojet_snapshots add column if not exists saved_at      timestamptz;
alter table public.gojet_snapshots add column if not exists atualizado_em timestamptz;

create index if not exists idx_gojet_snap_cidade_saved
  on public.gojet_snapshots (cidade, saved_at desc);

-- log_slots_auto: faltam data, slots_criados, detalhes
alter table public.log_slots_auto add column if not exists data          date;
alter table public.log_slots_auto add column if not exists slots_criados int;
alter table public.log_slots_auto add column if not exists detalhes      jsonb;

-- monitor_alertas: falta resolvido
alter table public.monitor_alertas add column if not exists resolvido boolean default false;
alter table public.monitor_alertas add column if not exists msg       text;

-- service_role full access nas tabelas existentes que podem não ter
do $$ begin
  if not exists (select 1 from pg_policies where tablename='gojet_snapshots' and policyname='gojet_snap_svc') then
    create policy gojet_snap_svc on public.gojet_snapshots for all to service_role using (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='monitor_alertas' and policyname='monalert_svc') then
    create policy monalert_svc on public.monitor_alertas for all to service_role using (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='log_slots_auto' and policyname='lsa_svc') then
    create policy lsa_svc on public.log_slots_auto for all to service_role using (true);
  end if;
end $$;

