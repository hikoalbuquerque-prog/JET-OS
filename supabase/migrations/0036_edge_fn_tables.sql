  tipo_monitor    text,
  config          jsonb not null default '{}'::jsonb,
  criado_em       timestamptz not null default now(),
  atualizado_em   timestamptz not null default now()
);
alter table public.monitor_config enable row level security;
do $$ begin
  if not exists (select 1 from pg_policies where tablename='monitor_config' and policyname='monconf_sel') then
    create policy monconf_sel on public.monitor_config for select using (auth.role() = 'authenticated');
  end if;
end $$;

-- ── monitor_alertas: alertas de GPS/SLA/geofence ──
create table if not exists public.monitor_alertas (
  id          bigint generated by default as identity primary key,
  tipo        text not null,
  uid         text,
  slot_id     text,
  tarefa_id   text,
  mensagem    text,
  dados       jsonb,
  criado_em   timestamptz not null default now()
);
create index if not exists idx_mon_alertas_tipo_uid on public.monitor_alertas(tipo, uid, criado_em desc);
alter table public.monitor_alertas enable row level security;
do $$ begin
  if not exists (select 1 from pg_policies where tablename='monitor_alertas' and policyname='monalert_sel') then
    create policy monalert_sel on public.monitor_alertas for select using (auth.role() = 'authenticated');
  end if;
  if not exists (select 1 from pg_policies where tablename='monitor_alertas' and policyname='monalert_ins') then
    create policy monalert_ins on public.monitor_alertas for insert with check (true);
  end if;
end $$;

