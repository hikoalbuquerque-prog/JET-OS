-- 0036 — Tabelas necessárias p/ Edge Functions portadas do Firebase Cloud Functions.
-- gojet_snapshots, monitor_config, monitor_alertas, config_auto_slots,
-- logs_automacao, log_slots_auto, tarefas (monitor).
-- Idempotente (CREATE IF NOT EXISTS).

-- ── gojet_snapshots: snapshots periódicos de parkings/bikes GoJet ──
create table if not exists public.gojet_snapshots (
  id         text primary key,
  data       jsonb not null default '[]'::jsonb,
  criado_em  timestamptz not null default now()
);
alter table public.gojet_snapshots enable row level security;
do $$ begin
  if not exists (select 1 from pg_policies where tablename='gojet_snapshots' and policyname='gojet_snap_sel') then
    create policy gojet_snap_sel on public.gojet_snapshots for select using (auth.role() = 'authenticated');
  end if;
end $$;

-- ── monitor_config: configuração de monitoramento por cidade ──
create table if not exists public.monitor_config (
