-- ── tarefas: tarefas de monitor (M1/M2/M3), distintas de tarefas_logistica ──
create table if not exists public.tarefas (
  id              text primary key default gen_random_uuid()::text,
  tipo            text,
  estacao_id      text,
  cidade          text,
  status          text not null default 'aberto',
  prioridade      text default 'normal',
  descricao       text,
  dados           jsonb,
  criado_por      text,
  atribuido_a     text,
  criado_em       timestamptz not null default now(),
  atualizado_em   timestamptz not null default now()
);
create index if not exists idx_tarefas_status on public.tarefas(status, cidade);
alter table public.tarefas enable row level security;
do $$ begin
  if not exists (select 1 from pg_policies where tablename='tarefas' and policyname='tarefas_sel') then
    create policy tarefas_sel on public.tarefas for select using (auth.role() = 'authenticated');
  end if;
  if not exists (select 1 from pg_policies where tablename='tarefas' and policyname='tarefas_ins') then
    create policy tarefas_ins on public.tarefas for insert with check (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='tarefas' and policyname='tarefas_upd') then
    create policy tarefas_upd on public.tarefas for update using (true);
  end if;
end $$;

