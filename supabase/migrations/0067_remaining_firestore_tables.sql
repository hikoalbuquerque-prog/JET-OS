-- ════════════════════════════════════════════════════════════════════════════
-- 1. COLUNAS FALTANTES EM TABELAS EXISTENTES
-- ════════════════════════════════════════════════════════════════════════════

-- slot_alertas: CF escreve cidade, vagas_reabertas, aceite_ids
alter table public.slot_alertas add column if not exists cidade           text;
alter table public.slot_alertas add column if not exists vagas_reabertas  int;
alter table public.slot_alertas add column if not exists aceite_ids       text[];
alter table public.slot_alertas add column if not exists slot_id_ref      text;
create index if not exists idx_slot_alertas_cidade on public.slot_alertas (cidade, criado_em desc);

-- slot_lembretes: CF escreve nome, cnpj, fase, min_restantes, cidade, aceite_id
alter table public.slot_lembretes add column if not exists aceite_id      text;
alter table public.slot_lembretes add column if not exists nome           text;
alter table public.slot_lembretes add column if not exists cnpj           text;
alter table public.slot_lembretes add column if not exists fase           int;
alter table public.slot_lembretes add column if not exists min_restantes  int;
alter table public.slot_lembretes add column if not exists cidade         text;
create index if not exists idx_slot_lembretes_cidade on public.slot_lembretes (cidade, criado_em desc);

-- eventos: campos GoJet point linking
alter table public.eventos add column if not exists nome                    text;
alter table public.eventos add column if not exists estado                  text;
alter table public.eventos add column if not exists pais                    text;
alter table public.eventos add column if not exists inicio                  timestamptz;
alter table public.eventos add column if not exists fim                     timestamptz;
alter table public.eventos add column if not exists ponto_gojet_estacao_id  text;
alter table public.eventos add column if not exists ponto_gojet_lat         numeric;
alter table public.eventos add column if not exists ponto_gojet_lng         numeric;
alter table public.eventos add column if not exists ponto_gojet_target      int;
alter table public.eventos add column if not exists ponto_gojet_raio        int;
alter table public.eventos add column if not exists firebase_id             text;
alter table public.eventos add column if not exists atualizado_em           timestamptz;
-- policy update
do $$ begin
  if not exists (select 1 from pg_policies where tablename='eventos' and policyname='eventos_upd') then
    create policy eventos_upd on public.eventos for update to authenticated using (true) with check (true);
  end if;
end $$;

