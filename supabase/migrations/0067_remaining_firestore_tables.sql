-- ── monitor_config ──────────────────────────────────────────────────────────
-- Configuração de monitor por cidade com 3 níveis (M1/M2/M3)
create table if not exists public.monitor_config (
  cidade             text primary key,
  m1                 jsonb default '{}'::jsonb,
  m2                 jsonb default '{}'::jsonb,
  m3                 jsonb default '{}'::jsonb,
  atualizado_em      timestamptz
);
alter table public.monitor_config enable row level security;
create policy mc_svc on public.monitor_config for all to service_role using (true);
create policy mc_sel on public.monitor_config for select to authenticated using (true);
create policy mc_ups on public.monitor_config for insert to authenticated with check (true);
create policy mc_upd on public.monitor_config for update to authenticated using (true) with check (true);

-- ── guard_config ────────────────────────────────────────────────────────────
-- Regiões + controle de perdas (antes 2 docs Firestore)
create table if not exists public.guard_regioes (
  cidade   text primary key,
  regiao   text,
  filial   text
);
alter table public.guard_regioes enable row level security;
create policy gr_svc on public.guard_regioes for all to service_role using (true);
create policy gr_sel on public.guard_regioes for select to authenticated using (true);
create policy gr_ups on public.guard_regioes for insert to authenticated with check (true);
create policy gr_upd on public.guard_regioes for update to authenticated using (true) with check (true);

create table if not exists public.guard_controle_perdas (
  id                bigint generated by default as identity primary key,
  filial            text,
  regiao            text,
  responsavel       text,
  patins            int default 0,
  bikes             int default 0,
  brpd              int default 0,
  vand_total        int default 0,
  nao_enc_patins    int default 0,
  nao_enc_bikes     int default 0,
  nao_enc_bat       int default 0,
  status1_24h       text,
  status2_7d        text,
  atualizado_em     timestamptz
);
alter table public.guard_controle_perdas enable row level security;
create policy gcp_svc on public.guard_controle_perdas for all to service_role using (true);
create policy gcp_sel on public.guard_controle_perdas for select to authenticated using (true);
create policy gcp_ups on public.guard_controle_perdas for insert to authenticated with check (true);
create policy gcp_upd on public.guard_controle_perdas for update to authenticated using (true) with check (true);
create policy gcp_del on public.guard_controle_perdas for delete to authenticated using (true);

-- ── operacoes ───────────────────────────────────────────────────────────────
-- CRUD de operações logísticas (antes via callable CFs)
create table if not exists public.operacoes (
  id              bigint generated by default as identity primary key,
  uid             text,
  tipo            text,
  status          text not null default 'pendente',
  prioridade      text,
  estacao_id      text,
  quantidade      int,
  notas           text,
  data_vencimento timestamptz,
  cidade          text,
  criado_em       timestamptz not null default now(),
  atualizado_em   timestamptz
);
create index if not exists idx_operacoes_cidade on public.operacoes (cidade, criado_em desc);
create index if not exists idx_operacoes_status on public.operacoes (status);
alter table public.operacoes enable row level security;
create policy op_svc on public.operacoes for all to service_role using (true);
create policy op_sel on public.operacoes for select to authenticated using (true);
create policy op_ins on public.operacoes for insert to authenticated with check (true);
create policy op_upd on public.operacoes for update to authenticated using (true) with check (true);
create policy op_del on public.operacoes for delete to authenticated using (true);

